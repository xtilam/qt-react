EnumDefine::EnumData *EnumDefine::EnumData::add(int enumValue, QString name)
{
    auto enumValueString = QString::number(enumValue);
    QString value = "";
    if(map.contains(enumValueString)){
        value = map.value(enumValueString).toString() + " || " + name;
    }else{
        value = name;
    }
    map.insert(enumValueString, value);
    map.insert(name, enumValue);
    return this;
}

EnumDefine::EnumData *EnumDefine::EnumData::setName(QString name)
{
    this->name = name;
    return this;
}

const QString EnumDefine::EnumData::lastData()
{
    QString result(this->name + "={");
    for(auto iter = map.begin(); iter != map.end(); iter++){
        auto value = iter.value();
        if(value.type() == QVariant::String){
            result.append(iter.key() + ":\"" + value.toString() + "\",");
        }else{
            result.append(iter.key() + ":" + QString::number(value.toInt()) + ",");
        }
    }
    result.append("}");
    delete this;
    return result;
}
